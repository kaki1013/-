N = int(input())
# 000-1, 100-2, 010-3, 001-4, 101-5
# 가능한 경우 1 -> {1, 2, 3, 4, 5}, 2 -> {1, 3, 4}, 3 -> {1, 2, 4, 5}, 4 -> {1, 2, 3}, 5 -> {1, 3}
# {1,2,3,4,5} -> 1, {1,3,4} -> 2, {1,2,4,5} -> 3, {1,2,3} -> 4, {1,3} -> 5
 
# 1, 2, 3, 4, 5 일 때 경우의 수
case = [1, 1, 1, 1, 1]	# init : 첫 열에는 모든 경우 가능
for _ in range(N-1):
	a, b, c, d, e = case
	s = sum(case)
	temp = [s, a+c+d, s-c, a+b+c, a+c]
	for i in range(5):
		temp[i] %= 100000007
	case = temp
print(sum(case) % 100000007)
